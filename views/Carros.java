        //Ler arquivo e preencher tabela
    public void preencheTabela(){
     try {
            DefaultTableModel modelo = (DefaultTableModel) tCarros.getModel();
            if (modelo.getRowCount() > 0){
                modelo.setRowCount(0);
            }
      
            String path = "C:\\Users\\mauri\\Documents\\NetBeansProjects\\Locadora\\src\\dados";
            String nomeArquivo = "carro.csv";
            File arquivo = new File(path,nomeArquivo);
            
            FileInputStream fis = new FileInputStream(arquivo);
            InputStreamReader isr = new InputStreamReader(fis);
            BufferedReader br = new BufferedReader(isr);
            
            String linha = br.readLine();
            while (linha != null){
                Object[] obj = linha.split(";");
                modelo.addRow(obj);
                linha = br.readLine();  
            }
            
            
        } catch (FileNotFoundException e){
            JOptionPane.showMessageDialog(null, "ERRO");
        } catch (IOException ex) {
            JOptionPane.showMessageDialog(null, "ERRO");
        }
}
