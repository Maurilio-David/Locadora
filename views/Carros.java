    private void jButton3ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton3ActionPerformed
     
    	//Altera os valores da Jtable na linha selecionada
    	if(tCarros.getSelectedRow() != -1){
            
           tCarros.setValueAt(txtId.getText(), tCarros.getSelectedRow(), 0);
           tCarros.setValueAt(txtModelo.getText(), tCarros.getSelectedRow(), 1);
           tCarros.setValueAt(txtTipo.getText(), tCarros.getSelectedRow(), 2);
           tCarros.setValueAt(txtCor.getText(), tCarros.getSelectedRow(), 3);
           tCarros.setValueAt(txtQtd.getText(), tCarros.getSelectedRow(), 4);
           tCarros.setValueAt(txtAno.getText(), tCarros.getSelectedRow(), 5);
           
        }
        
     //Faz a alteração no arquivo csv
     tCarros.getRowCount();
        String[] lines = new String[5];
        
        String path = "C:\\Users\\mauri\\Documents\\NetBeansProjects\\Locadora\\src\\dados\\carro.csv";
     try (BufferedWriter bw = new BufferedWriter(new FileWriter(path))) {
            
            for (int a = 0; a < 1; a++) {
                
                for(int i = 0; i<tCarros.getRowCount();i++){
                for(int j = 0; j<5;j++){
                    lines[j] = tCarros.getValueAt(i, j).toString();
                    System.out.println(lines[j]);  
                    bw.write(lines[j] + ";");
                    
                }
                bw.newLine();
            }    
            }
            
        } catch (IOException e) {
            e.printStackTrace();
        }
    }//GEN-LAST:event_jButton3ActionPerformed
    
    
