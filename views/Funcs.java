    private void jButton1ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton1ActionPerformed
    if(tFuncs.getSelectedRow() != -1){
            
           tFuncs.setValueAt(txtId.getText(), tFuncs.getSelectedRow(), 0);
           tFuncs.setValueAt(txtNome.getText(), tFuncs.getSelectedRow(), 1);
           tFuncs.setValueAt(txtCargo.getText(), tFuncs.getSelectedRow(), 2);
           tFuncs.setValueAt(txtLogin.getText(), tFuncs.getSelectedRow(), 3);
           tFuncs.setValueAt(txtSenha.getText(), tFuncs.getSelectedRow(), 4);
           tFuncs.setValueAt(txtMatricula.getText(), tFuncs.getSelectedRow(), 5);
           
        }
        tFuncs.getRowCount();
        String[] lines = new String[5];
        
        String path = "C:\\Users\\mauri\\Documents\\NetBeansProjects\\Locadora\\src\\dados\\func.csv";
     try (BufferedWriter bw = new BufferedWriter(new FileWriter(path))) {
            
            for (int a = 0; a < 1; a++) {
                
                for(int i = 0; i<tFuncs.getRowCount();i++){
                for(int j = 0; j<5;j++){
                    lines[j] = tFuncs.getValueAt(i, j).toString();
                    System.out.println(lines[j]);  
                    bw.write(lines[j] + ";");
                    
                }
                bw.newLine();
            }    
            }
            
        } catch (IOException e) {
            e.printStackTrace();
        }
    }//GEN-LAST:event_jButton1ActionPerformed
     
